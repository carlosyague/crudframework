CREATE OR REPLACE VIEW CIR_VIEW_PRESTAMOS_DIA
AS
select concat(p.X_PRESTAMO, 'N') as X_CIR_VIEW_PRESTAMOS_DIA, 'N' as L_DEVUELTO, r.r_autor AS T_AUTOR, r.r_titulo AS T_TITULO, b.t_biblioteca  AS T_BIBLIOTECA, et.t_tipo AS T_TIPO_EJEMPLAR from CIR_PRESTAMOS p, CIR_TIPO_PRESTAMO tp, CAT_EJEMPLARES e, CAT_EJEMPLAR_TIPO et, CAT_REGISTRO r, CAT_REGISTRO_X_CAT_EJEMPLAR rxe, ADM_BIBLIOTECAS b where tp.X_TIPO_PRESTAMO = p.T_PRESTAMO_X_T_PRESTAMO and tp.C_TIPO_PRESTAMO = 'SA' AND e.x_ejemplar=p.ejemplar AND e.eje_x_ejemplar_tipo=et.x_ejemplar_tipo AND e.x_ejemplar=rxe.eje_x_ejemplar AND r.x_registro=rxe.reg_x_registro AND e.bib_x_biblioteca=b.x_biblioteca  UNION ALL  select concat(ph.X_PRESTAMO_HIST, 'S') as X_CIR_VIEW_PRESTAMOS_DIA, 'S' as L_DEVUELTO, r.r_autor AS T_AUTOR, r.r_titulo AS T_TITULO, b.t_biblioteca, et.t_tipo AS T_TIPO_EJEMPLAR from CIR_PRESTAMOS_HIST ph, CIR_TIPO_PRESTAMO tp, CAT_EJEMPLARES e, CAT_EJEMPLAR_TIPO et, CAT_REGISTRO r, CAT_REGISTRO_X_CAT_EJEMPLAR rxe, ADM_BIBLIOTECAS b where tp.X_TIPO_PRESTAMO = ph.T_PRESTAMO_X_T_PRESTAMO and tp.C_TIPO_PRESTAMO = 'SA' and to_char(ph.F_DEVOLUCION,'DD/MM/YYYY') = to_char(CURRENT_DATE,'DD/MM/YYYY') AND e.x_ejemplar=ph.eje_x_ejempar AND e.eje_x_ejemplar_tipo=et.x_ejemplar_tipo AND e.x_ejemplar=rxe.eje_x_ejemplar AND r.x_registro=rxe.reg_x_registro AND e.bib_x_biblioteca=b.x_biblioteca;


